    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        HTTP http = new HTTP();
        while (true) {
            try {
                http.GetScotty(CS.apiLoc + "/quotes/add?authkey=" + AuthKey + "&person=" + URLEncoder.encode(this.QPerson.getText(), "UTF-8") + "&quote=" + URLEncoder.encode(this.QuoteText.getText(), "UTF-8"));
                cp.PopQuoteList();
                break;
            } catch (UnsupportedEncodingException | ParseException ex) {
                Logger.getLogger(addquote.class.getName()).log(Level.SEVERE, null, ex);
            }

        }

        this.dispose();

    }//GEN-LAST:event_jButton1ActionPerformed

